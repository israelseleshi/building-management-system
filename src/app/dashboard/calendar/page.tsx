const startDay = (d: Date) => new Date(d.getFullYear(), d.getMonth(), d.getDate())
const dateKey = (d: Date) => startDay(d).toISOString().slice(0, 10)
const sameDay = (a: Date, b: Date) => dateKey(a) === dateKey(b)
const startWeek = (d: Date) => {
  const x = startDay(d)
  const wd = x.getDay()
  x.setDate(x.getDate() + (wd === 0 ? -6 : 1 - wd))
  return x
}
const weekDays = (d: Date) => Array.from({ length: 7 }, (_, i) => {
  const x = new Date(startWeek(d))
  x.setDate(x.getDate() + i)
  return x
})
const monthGrid = (d: Date) => Array.from({ length: 42 }, (_, i) => {
  const x = new Date(startWeek(new Date(d.getFullYear(), d.getMonth(), 1)))
  x.setDate(x.getDate() + i)
  return x
})
const toInput = (d: Date) => new Date(d.getTime() - d.getTimezoneOffset() * 60000).toISOString().slice(0, 16)

